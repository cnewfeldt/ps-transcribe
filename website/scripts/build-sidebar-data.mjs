#!/usr/bin/env node
/**
 * Prebuild codegen: scans website/src/app/docs/*\/page.mdx, extracts each file's
 * `export const doc = { group, order, navTitle }`, and writes
 * src/components/docs/sidebar-data.generated.ts.
 *
 * Implements Phase 14 D-08 ("sidebar structure is build-time assembled from the
 * `doc` exports of each page.mdx") and ROADMAP SC-1 ("creating a new .mdx file
 * produces a live doc page without any other code changes").
 *
 * Run via the `predev` and `prebuild` npm lifecycle hooks added in package.json.
 * Safe to run when zero page.mdx files exist — writes an empty DISCOVERED_DOCS array.
 */
import { readdir, readFile, writeFile, mkdir } from 'node:fs/promises'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const HERE = dirname(fileURLToPath(import.meta.url))
const WEBSITE_ROOT = join(HERE, '..')
const DOCS_DIR = join(WEBSITE_ROOT, 'src', 'app', 'docs')
const OUT_DIR = join(WEBSITE_ROOT, 'src', 'components', 'docs')
const OUT_FILE = join(OUT_DIR, 'sidebar-data.generated.ts')

/** Extract the object literal body of `export const doc = { ... }` from MDX source.
 *  Narrow regex: expects the export to be the first top-level statement that matches.
 *  Handles single-line or multi-line object. Returns the inner object text (without braces). */
function extractDocLiteral(mdxSource) {
  // Match `export const doc = { ... }` where braces are balanced on one level of nesting.
  // We assume `doc` is a flat object of string/number literals (the contract from D-04).
  const m = /export\s+const\s+doc\s*=\s*\{([\s\S]*?)\}/.exec(mdxSource)
  if (!m) return null
  return m[1]
}

/** Pull a single string-or-number value out of the object-literal body. */
function pullValue(body, key) {
  // key: 'value' | key: "value" | key: 123
  const re = new RegExp(`${key}\\s*:\\s*(?:'([^']*)'|"([^"]*)"|(\\d+))`)
  const m = re.exec(body)
  if (!m) return null
  if (m[1] !== undefined) return m[1]
  if (m[2] !== undefined) return m[2]
  if (m[3] !== undefined) return Number(m[3])
  return null
}

async function main() {
  let entries = []
  try {
    const dirents = await readdir(DOCS_DIR, { withFileTypes: true })
    for (const d of dirents) {
      if (!d.isDirectory()) continue
      const slug = d.name
      const file = join(DOCS_DIR, slug, 'page.mdx')
      let src
      try {
        src = await readFile(file, 'utf8')
      } catch {
        continue // no page.mdx in this folder — skip
      }
      const body = extractDocLiteral(src)
      if (!body) {
        console.warn(`[build-sidebar-data] ${slug}/page.mdx has no \`export const doc\`; skipping.`)
        continue
      }
      const group = pullValue(body, 'group')
      const order = pullValue(body, 'order')
      const navTitle = pullValue(body, 'navTitle')
      if (typeof group !== 'string' || typeof order !== 'number' || typeof navTitle !== 'string') {
        console.warn(`[build-sidebar-data] ${slug}/page.mdx \`doc\` export is missing group/order/navTitle; skipping.`)
        continue
      }
      entries.push({ slug, href: `/docs/${slug}`, navTitle, group, order })
    }
  } catch (err) {
    if (err && err.code === 'ENOENT') {
      // docs dir does not exist yet (very first build before any page.mdx ships)
      entries = []
    } else {
      throw err
    }
  }

  await mkdir(OUT_DIR, { recursive: true })

  const banner = `// AUTO-GENERATED by scripts/build-sidebar-data.mjs. Do not edit by hand.\n// Source: each src/app/docs/*\\/page.mdx's \`export const doc\`. Regenerate via \`pnpm predev\` or \`pnpm prebuild\`.\n`
  const body = `export type DiscoveredDoc = { slug: string; href: string; navTitle: string; group: string; order: number }\n\nexport const DISCOVERED_DOCS: DiscoveredDoc[] = ${JSON.stringify(entries, null, 2)}\n`
  await writeFile(OUT_FILE, banner + '\n' + body, 'utf8')
  console.log(`[build-sidebar-data] wrote ${entries.length} entries to ${OUT_FILE}`)
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
